% The objective of this exercise is to try and guess the correct solution for a given configuration of the game of wordle
%
% The program is given a number of already made guesses and their result(the green/yellow/black color in the background of the Wordle grid)
% It then tries to guess the correct solution based on these guesses, by looking into a dictionary of possible solutions
%
% Sample imput:
% 3 guesses have been  made and they were
% WEARY
% PILLS
% VAGUE
%
% The results of these guesses were:
%  WEARY : A and E are in the word
%  PILLS : P is in the correct position and S is in the word
%  VAGUE : A and E are in the correct position and U is in the word
%
% In this case there is only 1 solution in the supplied dictionary: PAUSE

:- use_module(library(clpfd)).	% Finite domain constraints

%%%%%%%%%%%%%%%
% wordleSolve(Out, NGuesses, Guesses, GuessResult) 
% 
% Main predicate, solves the wordle puzzle
%
% Parameters
% -Out: the solution as a string
% -NGuesses: number of guesses taken (supplied by the user)
% -Guesses: previous guesses as a list of LOWERCASE strings (supplied by the user)
% -GuessResult: result of previous guesses, as an array of size (NGuesses,5) composed of 0,1,2
%  If a letter was wrong then the relative position in the array is 0, if a letter exists in the
%  solution but isn't in the correct position it's 1, if it's also in the correct position it's 2

wordleSolve(Out, NGuesses, Guesses, GuessResults):- 
    % Force the solution to have length 5
    length(Sol,5),
    
    % The number of string guesses and their result should be equal to the number of guesses taken
    length(Guesses, NGuesses),
    length(GuessResults, NGuesses),
    
    % Use an array of ints instead of a list of strings
    convertListOfStrings(CGuesses, Guesses),
    
    % The solution has to be one of the possible ones in the dictionary
    words5(Dictionary),
    member(Sol,Dictionary),
    
    % The solution as well as the words in the dictionary are represented as integers in the 1..26 range
    % Using char values would be more convenient but this way is more portable with the minizinc solution
    Sol ins 1..26,
    
    % Add all the constraint due to previous guesses
    previousGuessesConstraint(Sol, CGuesses, GuessResults),
    
    % Map the solution as a list of int into a string for visualization purposes
   	reprToString(Out, Sol).


%%%%%%%%%%%%%%%
% convertListOfStrings( NewList, ListOfStrings)
%
% Converts a list of strings into a list of lists of integers (which take value in 1..26)
% Parameters:
% -NewList: the result as an 'array' of ints, 'a' is converted into 1 for example
% -ListOfStrings: the list of strings to convert into ints

convertListOfStrings([],[]).

convertListOfStrings([FirstList |OtherList], [FirstString | OtherStrings]):-
    % Transform each string into an array of ints in the range 97..122
    string_codes(FirstString, IntList), 
    % Subtract 96 to each int in the list to obtain ints in the range 1..96
    maplist(fixIntCharRange, FirstList, IntList),
    convertListOfStrings(OtherList, OtherStrings).

% Support predicate to map ints from 97.122 into 1..26
% X is in 1..26, Y is in 97..122
fixIntCharRange(X,Y):- X is Y - 96 .


%%%%%%%%%%%%%%%
% reprToString(String, Repr)
%
% Converts the solution (list of ints) from a representation where the single characters are represented as
% ints in the range 1..26 into a string, for visualization purposes
% Parameters:
% -String: the result of the  conversion as a string
% -Repr: the list of ints in the range 1..26 to convert

reprToString(String, Repr):-
    % Add 96 to all ints in the array
    maplist(fixIntCharRangeB, Repr, CharString),
    % Transform it into a string
    string_codes(String, CharString).

% Support predicate to map ints from from 1..26 into 97..122
fixIntCharRangeB(X,Y):- Y is X + 96 .

%%%%%%%%%%%%%%%
% previousGuessesConstraint(Solution, Guesses, GuessResults)
%
% For each of the already made guesses constraint the value of the solution according
% to the result of the guess
% Parameters:
% -Solution: the result as a list of ints
% -Guesses: the list of guesses, an individual guess is a list of ints
% -GuessResults: the result of the guesses, an individual result is a list of numbers in (0,1,2)

previousGuessesConstraint(_, [], []).

previousGuessesConstraint(Sol, [FirstGuess | OtherGuesses], [FirstResult | OtherResult]):- 
    % Force the individual characters of the solution
    %  to either match the guess or not depending on the result
    maplist(matchingLetters,Sol, FirstGuess, FirstResult),
    % Force the inclusion constraint(when the result of a guess is 1)
    containedLetters(Sol, FirstGuess, FirstResult),
    previousGuessesConstraint(Sol, OtherGuesses, OtherResult).

%%%%%
% matchingLetters(SolutionLetter, GuessLetter, GuessResult)
% 
% Support predicate to be used with maplist to force constraints on the solution
% It enforces the following behaviour:
% -if the result is 2 then the SolutionLetter has to be equal to the GuessLetter
% -else (the result is 0 or 1) the solutionLetter has to be different to the GuessLetter

matchingLetters(SolLetter, GuessLetter, GuessResult) :- 
    GuessResult = 2, SolLetter #= GuessLetter .
matchingLetters(SolLetter, GuessLetter, GuessResult) :- 
    GuessResult = 1, SolLetter #\= GuessLetter .
matchingLetters(SolLetter, GuessLetter, GuessResult) :- 
    GuessResult = 0, SolLetter #\= GuessLetter .

%%%%%
% containedLetters(Solution, Guess, Result)
%
% Predicate to force all letters in a guess which had 1 as a result to be contained in the solution
% if the result for a letter was 0 or 2 it's ignored
% A letter which has 2 as a result is in the solution of course, but this was already taken care of

containedLetters(Sol, [GLetter | GOthers], [R | ROther]):- 
    R = 1, member(GLetter,Sol), 
    containedLetters(Sol, GOthers, ROther).

containedLetters(Sol, [_ | GOthers], [R | ROther]):- 
    R = 0,
    containedLetters(Sol, GOthers, ROther).

containedLetters(Sol, [_ | GOthers], [R | ROther]):- 
    R = 2,
    containedLetters(Sol, GOthers, ROther).

containedLetters(_,[],[]).

